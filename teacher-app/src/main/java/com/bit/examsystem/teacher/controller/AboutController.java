package com.bit.examsystem.teacher.controller;

import javafx.fxml.FXML;
import javafx.scene.control.TextArea;

public class AboutController {

    @FXML
    private TextArea aboutTextArea;

    @FXML
    public void initialize() {
        aboutTextArea.setText(getHelpText());
    }

    private String getHelpText() {
        return """
                线上考试系统 - 教师端使用说明
                ==================================

                欢迎使用本线上考试系统。本系统旨在为局域网环境下的教学和培训提供一个轻量、易用的考试解决方案。

                核心操作流程:
                ----------------------------------
                1. 启动服务 -> 2. 试卷管理 -> 3. 学生连接 -> 4. 开始考试 -> 5. 自动阅卷 -> 6. 查看成绩

                详细步骤说明:
                ----------------------------------

                [第一步: 启动服务]
                - 点击菜单栏 [服务器(_S)] -> [启动服务]。
                - 服务器将默认在 8888 端口开始监听，等待学生客户端连接。
                - 启动成功后，学生才能连接到教师端。

                [第二步: 准备试卷]
                - 点击菜单栏 [考试(_E)] -> [试卷管理...]。
                - 在弹出的窗口中，您可以：
                  - [新建考试]: 创建一份新的空白试卷。
                  - [删除考试]: 删除选中的试卷及其所有题目和学生答案。
                  - [添加/编辑/删除题目]: 在右侧题目列表中管理试卷的具体题目。
                  - [保存试卷]: 对试卷的任何修改（包括题目增删改），都必须点击此按钮才能保存到数据库。

                [第三步: 学生连接]
                - 指导学生打开学生端，输入教师机的IP地址（通常是局域网IP）、学号和姓名。
                - 学生成功连接后，会出现在主界面的 [在线学生列表] Tab 中。
                - 您可以在 [考试监控] Tab 中看到实时的在线学生总数。

                [第四步: 开始考试]
                - 切换到 [考试管理] Tab。
                - 从下拉框中选择您想要进行的考试。
                - 点击绿色的 [开始考试] 按钮。
                - 点击后，系统会自动切换到 [考试监控] Tab，并开始倒计时。
                - 同时，试卷会被安全地（不含答案）分发给所有在线的学生。

                [第五步: 自动阅卷]
                - 考试时间结束后，或您手动点击 [手动结束考试] 按钮后，考试状态会变为“考试已结束”。
                - 此时，[自动阅卷] 按钮将变为可用状态。
                - 点击 [自动阅卷]，系统将根据您在试卷管理中设置的正确答案，自动为所有已提交的客观题评分。

                [第六步: 查看/导出成绩]
                - 点击菜单栏 [成绩(_R)] -> [查看/导出成绩...]。
                - 在弹出的窗口中，从下拉框选择您想查看的考试。
                - 列表中会显示所有参加该场考试学生的总分。
                - 点击 [详细信息] 可以查看每个学生每道题的具体作答情况和得分。
                - 点击 [导出为 CSV] 可以将总分列表保存为 .csv 文件，方便用 Excel 等软件打开。

                ==================================
                祝您使用愉快！
                """;
    }
}